// Funzioni per SOMMA SOTTRAZIONE MOLTIPLICAZIONE DIVISIONE:
int num1, num2, operazione, risultato;
float n1, n2;

int add(int num1, int num2) {
    return num1 + num2;
}
int sub(int num1, int num2) {
    return num1 - num2;
}
int mul(int num1, int num2) {
    return num1 * num2;
}
float divi(float n1, float n2) {
    return n1 / n2;
}
